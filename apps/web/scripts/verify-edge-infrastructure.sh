# Vercel Edge Infrastructure Verification Script
# Tests all Edge features and reports status

set -e

GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
NC='\033[0m' # No Color

PASSED=0
FAILED=0
WARNINGS=0

echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
echo -e "${BLUE}рџ”Ќ Edge Infrastructure Verification${NC}"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
echo ""

# Get deployment URL
if [ -n "$1" ]; then
    DEPLOY_URL="$1"
else
    echo "Reading deployment URL from Vercel..."
    DEPLOY_URL=$(vercel ls --json 2>/dev/null | grep -o '"url":"[^"]*"' | head -1 | cut -d'"' -f4 || echo "")
fi

if [ -z "$DEPLOY_URL" ]; then
    echo -e "${YELLOW}вљ пёЏ  No deployment URL provided${NC}"
    echo "Usage: $0 <deployment-url>"
    echo "Example: $0 your-app.vercel.app"
    exit 1
fi

echo -e "${BLUE}рџЊђ Testing: https://$DEPLOY_URL${NC}"
echo ""

# Test 1: Check if site is accessible
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
echo -e "${BLUE}Test 1: Site Accessibility${NC}"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
HTTP_CODE=$(curl -o /dev/null -s -w "%{http_code}" "https://$DEPLOY_URL")
if [ "$HTTP_CODE" -eq 200 ] || [ "$HTTP_CODE" -eq 301 ] || [ "$HTTP_CODE" -eq 302 ]; then
    echo -e "${GREEN}вњ… Site is accessible (HTTP $HTTP_CODE)${NC}"
    ((PASSED++))
else
    echo -e "${RED}вќЊ Site returned HTTP $HTTP_CODE${NC}"
    ((FAILED++))
fi
echo ""

# Test 2: Edge Middleware headers
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
echo -e "${BLUE}Test 2: Edge Middleware Headers${NC}"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
HEADERS=$(curl -s -I "https://$DEPLOY_URL" 2>/dev/null)

# Check geolocation headers
if echo "$HEADERS" | grep -qi "x-geo-country"; then
    GEO_COUNTRY=$(echo "$HEADERS" | grep -i "x-geo-country" | cut -d: -f2- | tr -d '[:space:]')
    echo -e "${GREEN}вњ… Geolocation headers present (Country: $GEO_COUNTRY)${NC}"
    ((PASSED++))
else
    echo -e "${YELLOW}вљ пёЏ  Geolocation headers not found${NC}"
    echo "   This is normal for local/preview deployments"
    ((WARNINGS++))
fi

# Check feature flags status
if echo "$HEADERS" | grep -qi "x-feature-flags-status"; then
    FLAGS_STATUS=$(echo "$HEADERS" | grep -i "x-feature-flags-status" | cut -d: -f2- | tr -d '[:space:]')
    echo -e "${GREEN}вњ… Feature flags status: $FLAGS_STATUS${NC}"
    ((PASSED++))
else
    echo -e "${YELLOW}вљ пёЏ  Feature flags status header not found${NC}"
    ((WARNINGS++))
fi
echo ""

# Test 3: Security headers
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
echo -e "${BLUE}Test 3: Security Headers${NC}"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"

SECURITY_HEADERS=(
    "strict-transport-security"
    "x-content-type-options"
    "x-frame-options"
    "referrer-policy"
    "permissions-policy"
)

for header in "${SECURITY_HEADERS[@]}"; do
    if echo "$HEADERS" | grep -qi "$header"; then
        echo -e "${GREEN}вњ… $header${NC}"
        ((PASSED++))
    else
        echo -e "${YELLOW}вљ пёЏ  $header not found${NC}"
        ((WARNINGS++))
    fi
done
echo ""

# Test 4: Node.js version
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
echo -e "${BLUE}Test 4: Build Configuration${NC}"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"

if [ -f "../../vercel.json" ]; then
    NODE_VERSION=$(grep -o '"nodeVersion"[[:space:]]*:[[:space:]]*"[^"]*"' ../../vercel.json | cut -d'"' -f4)
    if [ "$NODE_VERSION" = "24.x" ]; then
        echo -e "${GREEN}вњ… Node.js 24.x configured${NC}"
        ((PASSED++))
    else
        echo -e "${RED}вќЊ Node.js version: $NODE_VERSION (expected 24.x)${NC}"
        ((FAILED++))
    fi
else
    echo -e "${YELLOW}вљ пёЏ  vercel.json not found${NC}"
    ((WARNINGS++))
fi
echo ""

# Test 5: Package installations
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
echo -e "${BLUE}Test 5: Required Packages${NC}"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"

if [ -f "package.json" ]; then
    if grep -q "@vercel/edge-config" package.json; then
        echo -e "${GREEN}вњ… @vercel/edge-config installed${NC}"
        ((PASSED++))
    else
        echo -e "${RED}вќЊ @vercel/edge-config not found${NC}"
        ((FAILED++))
    fi
    
    if grep -q "@vercel/kv" package.json; then
        echo -e "${GREEN}вњ… @vercel/kv installed${NC}"
        ((PASSED++))
    else
        echo -e "${RED}вќЊ @vercel/kv not found${NC}"
        ((FAILED++))
    fi
    
    if grep -q "@vercel/analytics" package.json; then
        echo -e "${GREEN}вњ… @vercel/analytics installed${NC}"
        ((PASSED++))
    else
        echo -e "${YELLOW}вљ пёЏ  @vercel/analytics not found${NC}"
        ((WARNINGS++))
    fi
else
    echo -e "${RED}вќЊ package.json not found${NC}"
    ((FAILED++))
fi
echo ""

# Test 6: TypeScript compilation
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
echo -e "${BLUE}Test 6: TypeScript Compilation${NC}"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"

if pnpm run typecheck >/dev/null 2>&1; then
    echo -e "${GREEN}вњ… TypeScript compilation successful${NC}"
    ((PASSED++))
else
    echo -e "${RED}вќЊ TypeScript compilation failed${NC}"
    echo "   Run 'pnpm typecheck' for details"
    ((FAILED++))
fi
echo ""

# Test 7: Critical files
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
echo -e "${BLUE}Test 7: Critical Files${NC}"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"

CRITICAL_FILES=(
    "middleware.ts"
    "lib/edge-config.ts"
    "lib/kv-store.ts"
    "lib/analytics.ts"
    "vercel.json"
)

for file in "${CRITICAL_FILES[@]}"; do
    if [ -f "$file" ]; then
        echo -e "${GREEN}вњ… $file${NC}"
        ((PASSED++))
    else
        echo -e "${RED}вќЊ $file not found${NC}"
        ((FAILED++))
    fi
done
echo ""

# Test 8: Environment variables
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
echo -e "${BLUE}Test 8: Environment Configuration${NC}"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"

if [ -f ".env.local" ] || [ -f ".env" ]; then
    echo -e "${GREEN}вњ… Environment file present${NC}"
    ((PASSED++))
    
    ENV_FILE=".env.local"
    [ ! -f "$ENV_FILE" ] && ENV_FILE=".env"
    
    # Check for Edge Config
    if grep -q "EDGE_CONFIG" "$ENV_FILE" 2>/dev/null; then
        echo -e "${GREEN}вњ… EDGE_CONFIG configured${NC}"
        ((PASSED++))
    else
        echo -e "${YELLOW}вљ пёЏ  EDGE_CONFIG not set (using fallbacks)${NC}"
        ((WARNINGS++))
    fi
    
    # Check for KV
    if grep -q "KV_" "$ENV_FILE" 2>/dev/null; then
        echo -e "${GREEN}вњ… KV variables configured${NC}"
        ((PASSED++))
    else
        echo -e "${YELLOW}вљ пёЏ  KV variables not set (using memory cache)${NC}"
        ((WARNINGS++))
    fi
else
    echo -e "${YELLOW}вљ пёЏ  No environment file found${NC}"
    echo "   Run './scripts/setup-vercel-edge.sh' to configure"
    ((WARNINGS++))
fi
echo ""

# Summary
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
echo -e "${BLUE}рџ“Љ Verification Summary${NC}"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"
echo ""
echo -e "${GREEN}вњ… Passed: $PASSED${NC}"
echo -e "${YELLOW}вљ пёЏ  Warnings: $WARNINGS${NC}"
echo -e "${RED}вќЊ Failed: $FAILED${NC}"
echo ""

TOTAL=$((PASSED + WARNINGS + FAILED))
SCORE=$((PASSED * 100 / TOTAL))

echo "Score: $SCORE% ($PASSED/$TOTAL tests passed)"
echo ""

if [ $FAILED -eq 0 ] && [ $WARNINGS -le 3 ]; then
    echo -e "${GREEN}рџЋ‰ Edge infrastructure is properly configured!${NC}"
    exit 0
elif [ $FAILED -eq 0 ]; then
    echo -e "${YELLOW}вљ пёЏ  Edge infrastructure is working with some warnings${NC}"
    echo "Review warnings above for optional improvements"
    exit 0
else
    echo -e "${RED}вќЊ Edge infrastructure has critical issues${NC}"
    echo "Fix the failed tests above"
    exit 1
fi
