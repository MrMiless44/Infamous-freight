## Partnership Mechanics

### Lead Routing & SLA

```
LEAD SERVICE LEVEL AGREEMENT (SLA):

Inbound leads from partner:
  1. Received by Infamous SDR
  2. Qualified within 24 hours
  3. If qualified: Scheduled for demo within 3 days
  4. Monthly reporting to partner

Metrics to track:
  • Leads received: [X] (committed: 100+/month)
  • Leads qualified: [X]% (target: 50%+)
  • Leads closed: [X]% (target: 20-25%)
  • Time to demo: [X] days (target: <3 days)
  • Deal velocity: [X] days (target: <14 days)
```

### Revenue Share Models

```
MODEL 1: Marketplace/App Store Revenue Share
  • Partner (app store) takes: 30% of first-year ARR
  • Infamous keeps: 70%
  • Example: $100 customer = $30 to QB, $70 to Infamous
  • Duration: All future MRR (sticky commission)
  • Used for: QuickBooks, Shopify, others with app stores

MODEL 2: Co-Sales Commission
  • Partner (sale referee) gets: $500 per deal (flat fee)
  • OR: 10% of first-year ACV
  • Example: $1,500 customer = $150-500 to partner
  • Duration: One-time per deal
  • Used for: Enterprise partnerships (NetSuite, Flexport)

MODEL 3: Pure Co-Marketing (No Revenue Share)
  • No ongoing revenue sharing
  • Focus: Webinars, content, co-branded materials
  • Benefit: Mutual lead generation (partner sends leads, Infamous sends leads)
  • Used for: Complementary products (Slack, Zapier)
```

